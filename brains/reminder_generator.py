    user_prompt = f"–¢–∏–ø —Ñ—Ä–∞–∑—ã: {phrase_type}\n–ù–∞—Å—Ç—Ä–æ–µ–Ω–∏–µ –ö–∞—Ä–∏–Ω—ã: {current_emotion}"
