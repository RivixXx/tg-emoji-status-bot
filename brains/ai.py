    # –ü–µ—Ä–µ–¥–∞–µ–º chat_id –¥–ª—è —Ñ–∏–ª—å—Ç—Ä–∞—Ü–∏–∏ –ø–∞–º—è—Ç–∏ (SaaS ready)
    context_memory = await search_memories(prompt, user_id=chat_id)

