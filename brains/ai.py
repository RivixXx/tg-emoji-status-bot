async def _process_tool_calls(tool_calls: list, chat_id: int) -> str:
    """
    –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç tool calls –æ—Ç AI

    Args:
        tool_calls: –°–ø–∏—Å–æ–∫ –≤—ã–∑–æ–≤–æ–≤ –∏–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç–æ–≤ –æ—Ç Mistral
        chat_id: ID —á–∞—Ç–∞ –¥–ª—è –∫–æ–Ω—Ç–µ–∫—Å—Ç–∞

    Returns:
        –†–µ–∑—É–ª—å—Ç–∞—Ç –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è –∏–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç–æ–≤
    """
    from brains.ai_tools import tool_executor

    tool_responses = []

    for tool_call in tool_calls:
        func_name = tool_call["function"]["name"]
        args = json.loads(tool_call["function"]["arguments"])

        # –í—ã–ø–æ–ª–Ω—è–µ–º –∏–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç —á–µ—Ä–µ–∑ executor
        tool_result = await tool_executor.execute_tool(
            tool_name=func_name,
            args=args,
            user_id=chat_id
        )

        tool_responses.append(tool_result)

    # –û–±—ä–µ–¥–∏–Ω—è–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –≤—Å–µ—Ö –∏–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç–æ–≤
    final_result = "\n\n".join(tool_responses)
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤ –∏—Å—Ç–æ—Ä–∏—é —á–µ—Ä–µ–∑ –∫—ç—à
    chat_history = await chat_history_cache.get(chat_id)
    chat_history.append({"role": "assistant", "content": final_result})
    await chat_history_cache.set(chat_id, chat_history)

    return final_result
