             return "–û–π, —è –∫–∞–∂–µ—Ç—Å—è –Ω–µ–º–Ω–æ–≥–æ –ø–µ—Ä–µ—É—Ç–æ–º–∏–ª–∞—Å—å... üß†üí® –î–∞–π –º–Ω–µ –º–∏–Ω—É—Ç–∫—É –ø—Ä–∏–π—Ç–∏ –≤ —Å–µ–±—è, –∏ —è —Å–Ω–æ–≤–∞ –±—É–¥—É –≥–æ—Ç–æ–≤–∞ –±–æ–ª—Ç–∞—Ç—å!"

